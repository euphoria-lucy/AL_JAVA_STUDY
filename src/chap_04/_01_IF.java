package chap_04;

public class _01_IF {
    public static void main(String[] args) {

        // 조건문 IF

        int hour = 65;

        if (hour < 37)
            System.out.println("아기짐승 홈런");

        if (hour > 37) {
            System.out.println("아기짐승 홈런");
            System.out.println("말랑콩떡 안타");
        }
        System.out.println("이브이 승리!");
    }
}
